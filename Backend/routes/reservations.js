/**
 * GET /my/:userId
 * ‡∏î‡∏∂‡∏á‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏à‡∏≠‡∏á‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏Ç‡∏≠‡∏á‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ
 * ‡∏£‡∏ß‡∏°‡∏ó‡∏±‡πâ‡∏á‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡πá‡∏ô‡πÄ‡∏à‡πâ‡∏≤‡∏Ç‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡∏à‡∏≠‡∏á ‡πÅ‡∏•‡∏∞‡∏ó‡∏µ‡πà‡∏ñ‡∏π‡∏Å‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÄ‡∏õ‡πá‡∏ô‡∏™‡∏°‡∏≤‡∏ä‡∏¥‡∏Å
 */
router.get("/my/:userId", async (req, res) => {
  const { userId } = req.params;
  console.log(`üì© [GET MY] userId=${userId}`);

  try {
    // ‡∏ï‡πâ‡∏≠‡∏á‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏Å‡∏≤‡∏£‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡∏à‡∏≤‡∏Å rm.email ‡πÑ‡∏õ‡∏≠‡πâ‡∏≤‡∏á‡∏≠‡∏¥‡∏á‡∏Å‡∏±‡∏ö users.code_user
    // ‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ subquery ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏´‡∏≤ email ‡∏Ç‡∏≠‡∏á‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏à‡∏≤‡∏Å user_id
    const [rows] = await pool.query(
      `
¬† ¬† ¬† SELECT r.reservation_id, r.room_id, r.start_time, r.end_time, rm.name as member_name
¬† ¬† ¬† FROM reservations r
¬† ¬† ¬† JOIN reservation_members rm ON r.reservation_id = rm.reservation_id
¬† ¬† ¬† WHERE r.user_id = ? 
        OR rm.email = (SELECT email FROM users WHERE user_id = ?) 
¬† ¬† `,
      [userId, userId] // <--- ‡πÅ‡∏Å‡πâ‡πÄ‡∏á‡∏∑‡πà‡∏≠‡∏ô‡πÑ‡∏Ç rm.email IN (...) ‡πÄ‡∏õ‡πá‡∏ô rm.email = (...) ‡πÄ‡∏û‡∏£‡∏≤‡∏∞‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡∏´‡∏≤ email ‡∏Ç‡∏≠‡∏á‡πÄ‡∏à‡πâ‡∏≤‡∏Ç‡∏≠‡∏á user_id ‡∏ó‡∏µ‡πà‡∏™‡πà‡∏á‡∏°‡∏≤
    );

    console.log(`‚úÖ [GET MY] Found ${rows.length} reservations`);
    res.json(rows);
  } catch (err) {
    console.error("‚ùå [GET MY] Error:", err.message);
    res.status(500).json({ error: "Server error" });
  }
});

